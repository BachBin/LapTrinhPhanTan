    private void btnSendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSendActionPerformed
        String hostname = "";
        InetAddress ip;
        try {
            ip = InetAddress.getLocalHost();
            hostname = ip.getHostName();
            gn.Gui(txtSend.getText().trim(), hostname, Integer.parseInt(txtPort.getText().trim()));
            txtSend.setText("");
        } catch (UnknownHostException ex) {
           ex.printStackTrace();
        } catch (Exception ex) {
           ex.printStackTrace();
        }
        
       
    }//GEN-LAST:event_btnSendActionPerformed

    private void txtSendKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSendKeyPressed
        // TODO add your handling code here:
        try {
            String hostname = "";
            InetAddress ip;
            ip = InetAddress.getLocalHost();
            hostname = ip.getHostName();
            if(evt.getKeyCode() == evt.VK_ENTER){
                if(txtSend.getText().equals("")) return;
                gn.Gui(txtSend.getText().trim(), hostname, Integer.parseInt(txtPort.getText().trim()));
                txtSend.setText("");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }        
        
    }//GEN-LAST:event_txtSendKeyPressed

