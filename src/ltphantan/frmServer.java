    AutoSend robot = new AutoSend();
    
    public class TuyenNhan extends Thread{
        public void run(){
            while(true){
                try {
                    String st = gn.Nhan();
                    if(st != null && !st.isEmpty()){
                        String[] ds = st.split("[_]");
                        ArrayList<String> result = robot.exec(ds[1]);
                        
                        if(result.size() > 0){
                            for(String s: result){
                                Send(s);
                            }
                        }
                        list.add(ds[1]);
                    }
                } catch (Exception e) {
                    e.printStackTrace();
                }
            }
        }
    }
     
