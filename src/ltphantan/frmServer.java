    private void Send(String mess){
        try {
            String ip = InetAddress.getLocalHost().getHostAddress();
            gn.Gui(ip+"_"+mess, ip, Integer.parseInt(txtpor2.getText().trim()));
        } catch (Exception e) {
            e.printStackTrace();
        }
        txtSend1.setText("");
    }
